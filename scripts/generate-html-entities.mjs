import { writeFile } from 'node:fs/promises'
import process from 'node:process'

const SPEC_URL = 'https://html.spec.whatwg.org/entities.json'

// Preserve mdream's existing HTML4/common set, then cheaply deepen coverage
// with aliases, all legacy semicolonless names, ASCII references, and a small
// set of references needed by real-world text conversion. Shipping all 2,125
// names adds roughly 10 kB gzip to the JavaScript parser bundle.
const CORE_NAMES = new Set(`
  aacute Aacute acirc Acirc acute aelig AElig agrave Agrave alefsym alpha Alpha amp and ang apos
  aring Aring asymp atilde Atilde auml Auml bdquo beta Beta brvbar bull cap ccedil Ccedil cedil
  cent chi Chi circ clubs cong copy crarr cup curren dagger Dagger darr dArr deg delta Delta diams
  divide eacute Eacute ecirc Ecirc egrave Egrave empty epsilon Epsilon equiv eta Eta eth ETH euml
  Euml euro exist fnof forall frac12 frac14 frac34 frasl gamma Gamma ge gt harr hArr hearts hellip
  iacute Iacute icirc Icirc iexcl igrave Igrave image infin int iota Iota iquest isin iuml Iuml
  kappa Kappa lambda Lambda lang laquo larr lArr lceil ldquo le lfloor lowast loz lsaquo lsquo lt
  macr mdash micro middot minus mu Mu nabla nbsp ndash ne ni not notin nsub ntilde Ntilde nu Nu
  oacute Oacute ocirc Ocirc oelig OElig ograve Ograve oline omega Omega omicron Omicron oplus or
  ordf ordm oslash Oslash otilde Otilde otimes ouml Ouml para part permil perp phi Phi pi Pi piv
  plusmn pound prime Prime prod prop psi Psi quot radic rang raquo rarr rArr rceil rdquo real reg
  rfloor rho Rho rsaquo rsquo sbquo scaron Scaron sdot sect sigma Sigma sigmaf sim spades sub sube
  sum sup sup1 sup2 sup3 supe szlig tau Tau there4 theta Theta thetasym thorn THORN tilde times trade
  uacute Uacute uarr uArr ucirc Ucirc ugrave Ugrave uml upsih upsilon Upsilon uuml Uuml weierp xi Xi
  yacute Yacute yen yuml Yuml zeta Zeta
`.trim().split(/\s+/))

const CURATED_NAMES = new Set(`
  CounterClockwiseContourIntegral NotEqualTilde NotSubset ThickSpace ThinSpace
  emsp ensp fjlig lrm rlm shy thinsp zwj zwnj
`.trim().split(/\s+/))

function singleQuoted(value) {
  return `'${value
    .replaceAll('\\', '\\\\')
    .replaceAll('\'', '\\\'')
    .replaceAll('\n', '\\n')
    .replaceAll('\r', '\\r')
    .replaceAll('\t', '\\t')
    .replaceAll('\u2028', '\\u2028')
    .replaceAll('\u2029', '\\u2029')}'`
}

const response = await fetch(SPEC_URL)
if (!response.ok)
  throw new Error(`Failed to fetch ${SPEC_URL}: ${response.status}`)

const source = await response.json()
const canonical = new Map()
const legacyNames = new Set()

for (const [identifier, data] of Object.entries(source)) {
  if (identifier.endsWith(';'))
    canonical.set(identifier.slice(1, -1), data.characters)
  else
    legacyNames.add(identifier.slice(1))
}

const coreValues = new Set([...CORE_NAMES].map(name => canonical.get(name)))
const selected = [...canonical]
  .filter(([name, value]) =>
    CORE_NAMES.has(name)
    || legacyNames.has(name)
    || coreValues.has(value)
    || [...value].every(character => character.codePointAt(0) <= 0x7F)
    || CURATED_NAMES.has(name))
  .sort(([a], [b]) => a < b ? -1 : a > b ? 1 : 0)

const missing = [...CORE_NAMES, ...CURATED_NAMES].filter(name => !canonical.has(name))
if (missing.length)
  throw new Error(`Unknown named character references: ${missing.join(', ')}`)

const js = `// Generated by scripts/generate-html-entities.mjs from the WHATWG table.
// This size-conscious subset includes every legacy semicolonless reference,
// the prior mdream set and its aliases, ASCII references, and curated additions.
export const HTML_ENTITIES: Readonly<Record<string, string>> = {
${selected.map(([name, value]) => `  ${name}: ${singleQuoted(value)},`).join('\n')}
}

export const LEGACY_ENTITY_NAMES = ${singleQuoted(`|${[...legacyNames].sort().join('|')}|`)}
export const MAX_ENTITY_NAME_LENGTH = ${Math.max(...selected.map(([name]) => name.length))}
export const MAX_LEGACY_ENTITY_NAME_LENGTH = ${Math.max(...[...legacyNames].map(name => name.length))}
`

const rust = `// Generated by scripts/generate-html-entities.mjs from the WHATWG table.
// Keep this file in parity with packages/js/src/entities.ts.

pub(super) const MAX_ENTITY_NAME_LENGTH: usize = ${Math.max(...selected.map(([name]) => name.length))};
pub(super) const MAX_LEGACY_ENTITY_NAME_LENGTH: usize = ${Math.max(...[...legacyNames].map(name => name.length))};

#[inline]
pub(super) fn lookup_named_entity(name: &[u8]) -> Option<&'static str> {
  match name {
${selected.map(([name, value]) => `    b${JSON.stringify(name)} => Some(${JSON.stringify(value)}),`).join('\n')}
    _ => None,
  }
}

#[rustfmt::skip]
#[inline]
pub(super) fn is_legacy_named_entity(name: &[u8]) -> bool {
  matches!(name,
${[...legacyNames].sort().map(name => `    b${JSON.stringify(name)}`).join(' |\n')}
  )
}
`

await Promise.all([
  writeFile(new URL('../packages/js/src/entities.ts', import.meta.url), js),
  writeFile(new URL('../crates/core/src/entities_generated.rs', import.meta.url), rust),
])

process.stdout.write(`Generated ${selected.length} canonical and ${legacyNames.size} legacy names.\n`)
